def add(n1, n2):
    return float(n1 + n2)
def sub(n1, n2):
    return float(n1 - n2)
def mul(n1, n2):
    return float(n1 * n2)
def div(n1, n2):
    return float(n1 / n2)

if __name__ == '__main__':
    print(add(3.14, 1.2))
    print(sub(3.14, 1.2))
    print(mul(3.14, 1.2))
    print(div(3.14, 1.2))